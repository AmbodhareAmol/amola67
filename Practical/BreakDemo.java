package Practical;

public class BreakDemo {

	public static void main(String[] args) {
		
		
		for(int i=5;i<=10;i++)
		   {
			
			  System.out.println(i);
			  
			  if(i<=3)
			    {
				  break;
			    }
			} 
		     
		   System.out.println();
		   
		   int num=1;
		   
		   while(num>=1) {
			   System.out.println(num);
			   
			   if(num==4) {
				   break;
			   }
			   
			   num++;
			    
		   }     
		   
		   
	}

}
